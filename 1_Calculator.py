a=int(input("Enter first Number: "))
operator=input("Enter the Operator: ")
b=int(input("Enter second Number: "))

if operator=="+" :
    print("Sum of Both Number is: ", a + b)
if operator=="-" :
    print("Subtraction of Both Number is: ", a - b)
if operator=="*" :
    print("Multiplication of Both Number is: ", a * b)
if operator=="/" :
    print("Division of Both Number is: ", a / b)
if operator=="%" :
    print("Modulus of Both Number is: ", a % b)
if operator=="**" :
    print("Exponent of Both Number is: ", a ** b)
